#!/usr/bin/env node
// scripts/lib/backlog-frontmatter-validator.cjs
//
// Self-contained backlog-brief frontmatter validator (Phase 06 D-02/D-04/D-05/D-06).
//
// The enforcement core of cross-repo's [XR] backlog-frontmatter pre-commit gate.
// Invoked by scripts/hooks/pre-commit.d/10-backlog-frontmatter.sh with the
// commit's staged .planning/backlog/*.md paths as argv. Validates each against
// the active-brief rule (D-05) or the terminal-subdir rule (D-04) and exits 1
// on any violation, 0 if every staged brief is clean.
//
// "Self-contained" here means NO runtime dependency on the skills repo (D-02 —
// skills' leaf delegates to backlog-regen.cjs, which cross-repo lacks, and would
// fail-OPEN inert). It DOES require the repo-local ./parse-frontmatter.cjs; the
// foreign-tree self-containment (inline parser) is Plan 02's scaffold concern
// (D-20), not this file's.
//
// Fail-mode discipline (D-06):
//   - parseFrontmatter(raw) === null (malformed/empty delimiters) -> BLOCK
//     (fail-CLOSED — an unparseable staged brief is exactly the drift the gate
//      exists to catch).
//   - a parseable brief that violates the contract (missing/empty required
//     field, or status not matching) -> BLOCK (fail-CLOSED).
//   - the wrapper handles fail-OPEN on toolchain absence (no node / no
//     validator) before this file ever runs.
//
// Parser contract (VERIFIED): an ABSENT key reads as `undefined`; an empty
// value (e.g. `status:`) reads as `''`. Both fail the present-and-non-empty
// check below. parse-frontmatter.cjs aliases target_milestone -> scope; this
// validator reads fm.target_milestone (the real field), NEVER fm.scope
// (Pitfall 4) — so a brief that sets `scope:` directly but omits
// `target_milestone:` is correctly BLOCKED.

'use strict';

const fs = require('fs');
const path = require('path');
// ----------------------------------------------------------------------------
// INLINED parse-frontmatter.cjs (Phase 06 D-20 — scaffold self-containment).
// GENERATED by conventions/scaffolds/backlog-frontmatter-gate/generate-payload.sh
// from cross-repo's scripts/lib/parse-frontmatter.cjs — DO NOT hand-edit; edit
// the source and re-run generate-payload.sh. The validator logic below is
// byte-identical to cross-repo's scripts/lib/backlog-frontmatter-validator.cjs;
// only the relative parse-frontmatter import has been internalized so the
// validator runs self-contained in a foreign target tree (Pitfall 5 / A2).
// ----------------------------------------------------------------------------
// scripts/lib/parse-frontmatter.cjs
//
// Shared YAML-frontmatter parser. Extracted from backlog-regen.cjs to close
// drift surface C16/F5 (cross-repo-reindex.cjs previously lifted this verbatim).
//
// Contract:
//   - Expects raw markdown content that starts with `---` on its own line.
//   - Returns null if no opening `---` or no closing `---` is found.
//   - Returns an object mapping `key -> value` for every line matching
//     `^([a-zA-Z_][a-zA-Z0-9_-]*):\s*(.*)$` between the delimiters.
//     (Key regex accepts hyphenated keys — `disable-model-invocation`,
//     `argument-hint`, etc. — per Phase 10 D-07, 2026-05-11. Non-breaking
//     against the 7 existing consumers which read underscored backlog
//     keys exclusively.)
//   - Strips matching outer single OR double quotes from the value, ONLY
//     when the inner content contains no additional quote of the same
//     type (so `'a' or 'b'` is preserved verbatim — Phase 10 WR-03).
//   - Lines that do not match the regex are silently skipped (preserves
//     behavior on blank lines, comments, list items, AND malformed YAML
//     such as empty-key `: value` lines or lines without a colon). This
//     helper is permissive by design — call YAML.parse() if strict
//     validation is required by the consumer. Phase 10 IN-04 (2026-05-11).
//
// EXPLICITLY DEFERRED (Phase 10 D-07): multiline `description:` values are
// NOT supported. Parser is line-by-line. No current skill or backlog brief
// uses multiline descriptions (verified pre-commit 2026-05-11). If a
// multiline use case appears, capture as a backlog brief — multiline
// support requires a state-machine rewrite, invasive without current trigger.

// Field-resolution alias map for brief-shape frontmatter (Phase 4 D-23 + D-25, 2026-05-20).
// Brief frontmatter uses `created` (ISO-8601) and `target_milestone` where the INDEX
// render() function reads `date` and `scope`. Map at parse time so callers see a
// uniform schema regardless of which frontmatter shape the source uses.
const ALIASES = {
  created: 'date',           // ISO-8601 timestamp → INDEX date field (T-split inline)
  target_milestone: 'scope', // milestone string → INDEX scope field
  // status enum (captured/active/done) passes through unchanged
};

const parseFrontmatter = function (raw) {
  if (typeof raw !== 'string') return null;
  // Expect YAML block delimited by `---` on its own line at the top.
  if (!raw.startsWith('---')) return null;
  // Split on \r?\n so CRLF-line-ending input (Windows-authored markdown,
  // git core.autocrlf=true on fresh clone) does not leave a trailing \r
  // on each line — which would cause the closing-delim scan below to
  // never match `'---'` (it would see `'---\r'`) and silently return null.
  // Phase 10 WR-02 (2026-05-11).
  const lines = raw.split(/\r?\n/);
  let end = -1;
  for (let i = 1; i < lines.length; i++) {
    if (lines[i] === '---') { end = i; break; }
  }
  if (end === -1) return null;
  const fm = {};
  for (const line of lines.slice(1, end)) {
    const m = line.match(/^([a-zA-Z_][a-zA-Z0-9_-]*):\s*(.*)$/);
    if (!m) continue;
    const key = m[1];
    let val = m[2].trim();
    // Strip outer single OR double quotes ONLY if the inner content does
    // not contain another quote of the same type. This prevents corruption
    // of values like `'a' or 'b'` (a sentence containing quotes) being
    // mistaken for a quoted-value pair and stripped to `a' or 'b`.
    // Phase 10 WR-03 (2026-05-11).
    if (val.length >= 2) {
      const first = val[0];
      const last = val[val.length - 1];
      if ((first === '"' && last === '"' && !val.slice(1, -1).includes('"')) ||
          (first === "'" && last === "'" && !val.slice(1, -1).includes("'"))) {
        val = val.slice(1, -1);
      }
    }
    fm[key] = val;
  }
  // D-25 (locked 2026-05-20): inline date-portion split inside the EXISTING ALIASES loop.
  // No separate ALIASES_TRANSFORM map. No conditional inspection of rendered output.
  // Only if target key not already present (don't clobber explicit values).
  for (const [src, dst] of Object.entries(ALIASES)) {
    if (fm[src] !== undefined && fm[dst] === undefined) {
      fm[dst] = (dst === 'date' && typeof fm[src] === 'string' && fm[src].includes('T'))
        ? fm[src].split('T')[0]
        : fm[src];
    }
  }
  return fm;
};

// Bounded active-status enum (D-05 / A1). Mirrors the cross-repo backlog-status
// convention the two authoritative lifecycle tools already encode —
// skills/scripts/backlog-close.cjs CLOSEABLE_STATUSES and backlog-regen.cjs
// CANONICAL_STATUS are both {'captured','in-progress',''} (see skills
// decisions.md § "Backlog Terminal States — Cross-Repo Convention"). `in-progress`
// is a real active state (a partially-built brief); the gate was the lone outlier
// rejecting it. Empty/absent status is handled separately as a soft signal, so it
// is not listed here. Trivial to extend further — add values to this allowlist.
const ALLOWED_ACTIVE_STATUS = ['captured', 'in-progress'];

// Terminal subdirs (D-04 — fixed to the subdir name, not discretion).
const TERMINAL_SUBDIRS = ['shipped', 'rejected', 'superseded'];

// present-and-non-empty: absent (undefined) and empty-string ('') both fail.
function present(v) {
  return typeof v === 'string' && v.trim() !== '';
}

// Classify a staged path (IN-01 — explicit, fail-CLOSED on an unknown subdir):
//   - { kind: 'terminal', subdir } for .planning/backlog/{shipped,rejected,
//     superseded}/<name>.md (a recognized terminal subdir).
//   - { kind: 'unknown-subdir', subdir } for a brief nested under ANY OTHER
//     subdir of .planning/backlog/ (e.g. a typo `ship/` for `shipped/`). This
//     is treated as a VIOLATION by main() rather than silently validated as
//     active — a mis-filed brief under an unrecognized subdir is exactly the
//     drift the gate exists to catch (fail-CLOSED, consistent with D-06). The
//     escape hatch for an intentional new subdir is to add it to
//     TERMINAL_SUBDIRS (then re-run generate-payload.sh) or `git commit
//     --no-verify`.
//   - { kind: 'active' } for a top-level .planning/backlog/<name>.md.
function classify(relPath) {
  // Normalize to forward slashes for a stable match regardless of platform.
  const norm = relPath.split(path.sep).join('/');
  const m = norm.match(/(?:^|\/)\.planning\/backlog\/([^/]+)\/[^/]+\.md$/);
  if (m) {
    if (TERMINAL_SUBDIRS.includes(m[1])) {
      return { kind: 'terminal', subdir: m[1] };
    }
    return { kind: 'unknown-subdir', subdir: m[1] };
  }
  return { kind: 'active' };
}

function main() {
  const paths = process.argv.slice(2);
  const violations = [];

  for (const p of paths) {
    let raw;
    try {
      raw = fs.readFileSync(p, 'utf8');
    } catch (err) {
      // Cannot read a path that git reported as staged — treat as a violation
      // (fail-CLOSED): a brief the gate cannot inspect must not slip through.
      violations.push(`${p}: cannot read staged brief (${err.code || err.message})`);
      continue;
    }

    const fm = parseFrontmatter(raw);
    if (fm === null) {
      // Malformed/empty delimiters — the null fail-CLOSED path (D-06),
      // distinct from a parseable-but-missing-key violation.
      violations.push(`${p}: unparseable frontmatter (no/invalid --- delimiters)`);
      continue;
    }

    const { kind, subdir } = classify(p);

    if (kind === 'unknown-subdir') {
      // IN-01 fail-CLOSED: a brief nested under an UNRECOGNIZED backlog subdir
      // (likely a typo, e.g. `ship/` for `shipped/`) is a violation, not a
      // silently-active brief. Add the subdir to TERMINAL_SUBDIRS (and re-run
      // generate-payload.sh) if it is a legitimate new terminal state.
      violations.push(
        `${p}: unrecognized backlog subdir '${subdir}/' — expected one of [${TERMINAL_SUBDIRS.join(', ')}] or a top-level active brief (typo? add '${subdir}' to TERMINAL_SUBDIRS if intentional)`);
    } else if (kind === 'terminal') {
      // D-04 terminal-subdir rule.
      if (fm.status !== subdir) {
        violations.push(
          `${p}: status must be '${subdir}' to match its subdir (got '${fm.status === undefined ? '<absent>' : fm.status}')`);
      }
      if (!present(fm.closed)) {
        violations.push(`${p}: terminal brief under ${subdir}/ requires a non-empty 'closed:' date`);
      }
      if (subdir === 'superseded' && !present(fm.superseded_by)) {
        violations.push(`${p}: superseded brief requires a non-empty 'superseded_by:'`);
      }
    } else {
      // D-05 active-brief rule.
      if (!ALLOWED_ACTIVE_STATUS.includes(fm.status)) {
        violations.push(
          `${p}: active status must be one of [${ALLOWED_ACTIVE_STATUS.join(', ')}] (got '${fm.status === undefined ? '<absent>' : fm.status}')`);
      }
      // Read target_milestone, NEVER the aliased scope (Pitfall 4).
      if (!present(fm.target_milestone)) {
        violations.push(`${p}: active brief requires a non-empty 'target_milestone:'`);
      }
    }
  }

  if (violations.length > 0) {
    for (const v of violations) {
      process.stderr.write(`backlog-frontmatter: ${v}\n`);
    }
    process.exit(1); // fail-CLOSED on any violation (D-06)
  }
  process.exit(0);
}

main();
